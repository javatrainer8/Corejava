package basics;

public class Switchconcept {
	 public static void main(String[] args) {

	int example  =2;
    switch (example) {
      case 1:
        System.out.println("Monday");
        break;
           
      case 2:
        System.out.println("Tuesday.... switch to here");
        break;
      case 3:
        System.out.println("Wednesday");
        break;
      case 4:
        System.out.println("Thursday");
        break;
      case 5:
        System.out.println("Friday  ... now it is switching to case 5");
        break;
      case 6:
        System.out.println("Saturday");
        break;
      case 7:
        System.out.println("Sunday");
        break;
    }
  }
}

	


